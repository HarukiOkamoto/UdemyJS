//apiとは・・・Application Programming Interface
//標準apiというものがある
//ブラウザの標準機能
// ・DOM API（Document Object Model）:
// HTMLやXML文書の構造を表現し、その構造にアクセスするためのAPI。例えば、document.getElementById や document.createElement などがあります。
// ・Event API:
// イベント処理に関連するAPI。DOM要素上でのイベント（クリック、キー入力など）のリスニングやハンドリングに使用されます。例えば、addEventListener メソッドがあります。
// ・AJAX API:
// 非同期JavaScriptとXML（Asynchronous JavaScript and XML）通信を行うためのAPI。主に XMLHttpRequest オブジェクトを使用し、サーバーとのデータの双方向通信が可能です。最近のWeb開発では、Fetch APIがより一般的に使用されるようになっています。
// ・Canvas API:
// グラフィックスを描画するためのAPI。<canvas> 要素を通じて2DグラフィックスやWebGLを使用して3Dグラフィックスを描画することができます。
// ・Web Storage API:
// ブラウザ上でデータを永続的に保存するためのAPI。localStorage や sessionStorage を介して、キーと値のペアを保存できます。
// ・Web Workers API:
// マルチスレッド処理をサポートするためのAPI。バックグラウンドで非同期処理を実行できます。
// ・Fetch API:
// ネットワークリクエストを行うためのAPI。XMLHttpRequestの後継として、PromiseベースのシンプルなAPIとして導入されました。
// ・WebRTC API:
// ブラウザ間のリアルタイム通信（Real-Time Communication）を実現するためのAPI。ビデオチャットや音声通話などに利用されます。

